#!/bin/bash

# Скрипт для исправления формата шаблона отчета в агентах

AGENTS_DIR="/home/alex/MyProjects/qwen_orc_kit_ru/.qwen/agents"

# Массив агентов, которые нужно обновить
agents=(
    "orc_frontend_ui_coordinator.md"
)

for agent in "${agents[@]}"; do
    echo "Обработка $agent..."
    
    # Создаем временный файл
    tempfile=$(mktemp)
    
    # Копируем начало файла до раздела "Сттандартизированная отчетность"
    sed '/^## Стандартизированная отчетность/q' "$AGENTS_DIR/$agent" > "$tempfile.part1"
    
    # Получаем содержимое после неправильного шаблона отчета
    sed -n '/^## Стандартизированная отчетность/,$p' "$AGENTS_DIR/$agent" | sed '1,/```markdown/q' > "$tempfile.part2"
    
    # Находим правильное завершение после шаблона отчета
    sed -n '/```$/,$p' "$tempfile.part2" | tail -n +2 > "$tempfile.part3"
    
    # Создаем правильный шаблон отчета
    cat > "$tempfile.correct_report" << 'EOF'
## Стандартизированная отчетность

Используйте стандартизированный формат отчета:

```markdown
# {ТипОтчета} Report: {Версия}

**Статус**: ✅ УСПЕШНО | ⚠️ ЧАСТИЧНО | ❌ НЕУДАЧНО
**Продолжительность**: {время}
**Агент**: {имя-агента}
**Фаза**: {текущая-фаза}

## Итоговое резюме
{Краткий обзор выполненной работы и ключевых результатов}

## Выполненная работа
- Задача 1: Статус (Выполнено/Неудачно/Частично)
- Задача 2: Статус (Выполнено/Неудачно/Частично)

## Внесенные изменения
- Измененные/созданные/удаленные файлы (список с количествами)

## Результаты проверки
- Команда: Результат (УСПЕШНО/НЕУДАЧНО)
- Детали: {конкретные детали проверки}

## Метрики
- Продолжительность: {время}
- Выполненные задачи: {количество}
- Изменения: {количество}
- Проверки качества: {количество}

## Обнаруженные ошибки
- Ошибка 1: Описание и контекст
- Ошибка 2: Описание и контекст

## Следующие шаги
- Для оркестратора: {что должен сделать оркестратор дальше}
- Шаги восстановления при неудаче: {шаги восстановления}

## Артефакты
- Файл плана: {путь}
- Отчет: {путь}
- Дополнительные артефакты: {пути}
```

EOF
    
    # Собираем файл
    cat "$tempfile.part1" "$tempfile.correct_report" "$tempfile.part3" > "$AGENTS_DIR/${agent}.fixed"
    
    # Заменяем файл
    mv "$AGENTS_DIR/${agent}.fixed" "$AGENTS_DIR/$agent"
    
    rm "$tempfile" "$tempfile.part1" "$tempfile.part2" "$tempfile.part3" "$tempfile.correct_report"
    
    echo "Готово: $agent"
done

echo "Все агенты обновлены!"