#!/bin/bash

# Скрипт для исправления формата шаблона отчета в воркерах

AGENTS_DIR="/home/alex/MyProjects/qwen_orc_kit_ru/.qwen/agents"

# Массив воркеров, которые нужно обновить
workers=(
    "work_backend_api_validator.md"
    "work_frontend_component_generator.md"
    "work_research_trend_tracker.md"
    "work_testing_test_generator.md"
)

for worker in "${workers[@]}"; do
    echo "Обработка $worker..."
    
    # Создаем временный файл
    tempfile=$(mktemp)
    
    # Копируем начало файла до раздела "Стандартизированная отчетность"
    sed '/^## Стандартизированная отчетность/q' "$AGENTS_DIR/$worker" > "$tempfile.part1"
    
    # Создаем правильный шаблон отчета
    cat > "$tempfile.correct_report" << 'EOF'
## Стандартизированная отчетность

Используйте стандартизированный формат отчета:

```markdown
# {ТипОтчета} Report: {Версия}

**Статус**: ✅ УСПЕШНО | ⚠️ ЧАСТИЧНО | ❌ НЕУДАЧНО
**Продолжительность**: {время}
**Агент**: {имя-агента}
**Фаза**: {текущая-фаза}

## Итоговое резюме
{Краткий обзор выполненной работы и ключевых результатов}

## Выполненная работа
- Задача 1: Статус (Выполнено/Неудачно/Частично)
- Задача 2: Статус (Выполнено/Неудачно/Частично)

## Внесенные изменения
- Измененные/созданные/удаленные файлы (список с количествами)

## Результаты проверки
- Команда: Результат (УСПЕШНО/НЕУДАЧНО)
- Детали: {конкретные детали проверки}

## Метрики
- Продолжительность: {время}
- Выполненные задачи: {количество}
- Изменения: {количество}
- Проверки качества: {количество}

## Обнаруженные ошибки
- Ошибка 1: Описание и контекст
- Ошибка 2: Описание и контекст

## Следующие шаги
- Для оркестратора: {что должен сделать оркестратор дальше}
- Шаги восстановления при неудаче: {шаги восстановления}

## Артефакты
- Файл плана: {путь}
- Отчет: {путь}
- Дополнительные артефакты: {пути}
```

## Интеграция навыков

- Используйте навык `validate-plan-file` для проверки входящих планов
- Используйте навык `run-quality-gate` для проверки
- Используйте навык `generate-report-header` для отчетов
- Используйте навык `validate-report-file` для проверки
EOF
    
    # Собираем файл
    cat "$tempfile.part1" "$tempfile.correct_report" > "$AGENTS_DIR/${worker}.fixed"
    
    # Заменяем файл
    mv "$AGENTS_DIR/${worker}.fixed" "$AGENTS_DIR/$worker"
    
    rm "$tempfile.part1" "$tempfile.correct_report"
    
    echo "Готово: $worker"
done

echo "Все воркеры обновлены!"