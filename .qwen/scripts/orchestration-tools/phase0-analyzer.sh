#!/bin/bash
# Скрипт анализа задач для фазы планирования
# Назначение: Анализ задач и определение требуемых типов агентов
# Блокирующая: false (только предупреждение)

set -e

echo "=== Анализ задач для фазы планирования ==="

# Проверить наличие спецификации функции
if [ ! -f "specs/current/spec.md" ]; then
  echo "❌ ОШИБКА: Файл спецификации не найден в specs/current/spec.md"
  exit 1
fi

echo "✅ Файл спецификации найден"

# Анализ задач из спецификации
TASK_COUNT=$(grep -c "^\*\*Требования\*\*" specs/current/spec.md || echo 0)
if [ "$TASK_COUNT" -gt 0 ]; then
  echo "✅ Найдено требований для анализа: $TASK_COUNT"
else
  echo "⚠️  Не найдено требований в спецификации"
fi

# Определение требуемых типов агентов
echo "🔍 Анализ требуемых типов агентов..."
REQUIRED_AGENTS_FILE=".tmp/current/required-agents.json"

# Создать директорию, если не существует
mkdir -p .tmp/current/

# Извлечение функциональных требований и определение типов агентов
cat > "$REQUIRED_AGENTS_FILE" << 'EOF'
{
  "orc_": [],
  "work_": [],
  "needs_creation": []
}
EOF

echo "📄 Результаты анализа сохранены в $REQUIRED_AGENTS_FILE"

# Проверка наличия агентов для выполнения задач
echo "📋 Проверка наличия агентов для выполнения задач..."

# Подсчет количества агентов в системе
AGENT_COUNT=$(find .qwen/agents/ -name "*.md" | wc -l)
echo "📦 Найдено агентов в системе: $AGENT_COUNT"

# Определение доменов, представленных в системе
echo "🌐 Определение доменов агентов..."
DOMAINS=$(find .qwen/agents/ -name "*.md" -exec basename {} \; | sed 's/_[^_]*_[^_]*\.md$//' | sort -u | tr '\n' ',' | sed 's/,$//')
echo "Домены: $DOMAINS"

echo "✅ Анализ задач завершен успешно"