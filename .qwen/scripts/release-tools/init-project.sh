#!/bin/bash
# Скрипт: .qwen/scripts/release-tools/init-project.sh
# Назначение: Инициализация нового проекта из шаблона

set -e  # Прерывать выполнение при ошибках

PROJECT_NAME="$1"
ORGANIZATION="$2"

if [ -z "$PROJECT_NAME" ]; then
    echo "Использование: $0 <имя-проекта> [организация]"
    echo "Пример: $0 my-new-project my-org"
    exit 1
fi

if [ -z "$ORGANIZATION" ]; then
    ORGANIZATION="organization"
fi

echo "=== Инициализация нового проекта: $PROJECT_NAME ==="

# Проверка, существует ли директория проекта
if [ -d "$PROJECT_NAME" ]; then
    echo "Ошибка: Директория $PROJECT_NAME уже существует"
    exit 1
fi

# Создание директории проекта
mkdir -p "$PROJECT_NAME"
cd "$PROJECT_NAME"

# Копирование файлов шаблона (предполагается, что скрипт запускается из шаблона)
echo "Копирование файлов шаблона..."

# Создание базовой структуры проекта
mkdir -p specs/001-initial-feature
mkdir -p state
mkdir -p docs/project-specific
mkdir -p .specify/memory
mkdir -p .specify/templates
mkdir -p .specify/scripts
mkdir -p .qwen/agents
mkdir -p .qwen/commands
mkdir -p .qwen/scripts/orchestration-tools
mkdir -p .qwen/scripts/specification-tools
mkdir -p .qwen/scripts/release-tools

# Создание обновленного README.md
cat > README.md << README_EOF
# $PROJECT_NAME

Добро пожаловать в $PROJECT_NAME - проект, созданный на основе шаблона Qwen Code Orchestrator Kit.

## Описание проекта

Этот проект создан с использованием шаблона Qwen Code Orchestrator Kit - инструмента для оркестрации задач разработки с использованием ИИ-агентов.

## Структура проекта

\`\`\`
.specify/                 # Шаблоны и скрипты для спецификаций
├── memory/              # Постоянная память для агентов
├── .qwen/scripts/             # Bash-скрипты для автоматизации
├── templates/           # Шаблоны для различных документов
└── ...
docs/                   # Документация проекта
├── architecture/        # Архитектурная документация
└── ...
.qwen/                  # Конфигурация Qwen CLI
├── agents/              # Агенты
└── commands/            # Команды
└── ...
specs/                  # Спецификации функциональности
.qwen/scripts/                # Скрипты для контрольных точек качества
state/                  # Состояние системы и артефакты выполнения задач
\`\`\`

## Начало работы

1. Убедитесь, что у вас установлены необходимые инструменты
2. Ознакомьтесь с архитектурной документацией в \`docs/architecture/\`
3. Изучите доступные агенты в \`.qwen/agents/\`
4. Попробуйте выполнить простую задачу с использованием оркестратора

## Использование

Для запуска процесса разработки на основе спецификаций:

1. Создайте конституцию проекта: \`speckit.constitution\`
2. Создайте спецификацию: \`speckit.specify "описание функции"\`
3. Уточните спецификацию: \`speckit.clarify\`
4. Создайте план: \`speckit.plan\`
5. Сгенерируйте задачи: \`speckit.tasks\`
6. Выполните реализацию: \`speckit.implement\`

Для получения более подробной информации см. документацию в \`docs/architecture/specification-driven-development.md\`.
README_EOF

# Создание обновленного файла конституции
cat > .specify/memory/constitution.md << CONSTITUTION_EOF
# Конституция проекта $PROJECT_NAME

## Основные принципы

