# Обновление состояния
update_state() {
    local state_file="$PROJECT_ROOT/specs/$SPEC_ID/state.json"
    
    log_info "Обновление состояния..."
    
    if command -v jq &> /dev/null && [ -f "$state_file" ]; then
        jq '.phase = "checklist_complete" | .commands.checklist = "completed"' \
            "$state_file" > "$state_file.tmp" && mv "$state_file.tmp" "$state_file"
    fi
    
    log_success "Состояние обновлено"
}

# Основная функция
main() {
    SPEC_ID="${1:-}"
    PROJECT_NAME="${2:-New Project}"
    
    if [ -z "$SPEC_ID" ]; then
        SPEC_ID=$(ls -t "$PROJECT_ROOT/specs/" 2>/dev/null | head -1)
        if [ -z "$SPEC_ID" ]; then
            log_error "SPEC_ID не указан и спецификации не найдены"
            exit 1
        fi
    fi
    
    echo "========================================"
    echo "  SpecKit: Checklist"
    echo "  Версия: 1.0.0"
    echo "  Spec ID: $SPEC_ID"
    echo "========================================"
    echo ""
    
    check_dependencies
    echo ""
    
    create_checklist
    echo ""
    
    create_acceptance_tests
    echo ""
    
    create_acceptance_criteria
    echo ""
    
    create_validation_report
    echo ""
    
    update_state
    echo ""
    
    log_success "========================================"
    log_success "  Чек-лист создан!"
    log_success "  Файлы:"
    log_success "  - checklist.md"
    log_success "  - acceptance-tests.md"
    log_success "  - acceptance-criteria.md"
    log_success "  - validation-report.md"
    log_success "  Следующий шаг: speckit.tasks"
    log_success "========================================"
}

# Запуск
main "$@"
