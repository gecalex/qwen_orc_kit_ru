#!/bin/bash
# Скрипт автоматического обновления индекса проекта
# Назначение: Обновление файла индекса проекта с информацией о структуре и компонентах

set -e

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Функция для вывода сообщений
log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

log_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

INDEX_FILE="docs/project-index.md"
PROJECT_ROOT="."

log_info "Начало обновления индекса проекта"

# Создаем директорию для индекса, если не существует
mkdir -p "$(dirname "$INDEX_FILE")"

# Начинаем формировать содержимое индекса
cat > "$INDEX_FILE" << 'EOF'
# Файл индекса проекта Qwen Orchestrator Kit

> **Назначение**: Этот файл помогает агентам понять структуру проекта без чтения всей кодовой базы. Реализует концепцию "ленивой загрузки знаний" без накладных расходов.
> **Обновлено**: $(date)

## Структура проекта

### `.qwen/` - Конфигурация агентов
- `agents/` - Директория для агентов
  - `orc_{domain}_{name}.md` - Оркестраторы
  - `work_{domain}_{name}.md` - Воркеры
- `skills/` - Директория для навыков
  - `{skill-name}/SKILL.md` - Навыки агентов
- `templates/` - Шаблоны для создания новых компонентов
- `configs/` - Конфигурации для MCP и других сервисов

### `scripts/` - Скрипты автоматизации
- `agent-creation/` - Скрипты для автоматического создания агентов
- `monitoring/` - Скрипты мониторинга и проверки соответствия
- `validation/` - Скрипты проверки стандартов
- `quality-gates/` - Скрипты контрольных точек качества
- `search/` - Скрипты поиска и навигации
- `analysis/` - Скрипты анализа компонентов

### `docs/` - Документация
- `architecture/` - Архитектурная документация
- `help/` - Справочная документация
- `next_step/` - Документация для следующих шагов
- `agents-index.md` - Индекс агентов
- `agent-categories.md` - Категории агентов
- `project-index.md` - Этот файл (индекс проекта)

### `specs/` - Спецификации функций
- `{###}-{feature-name}/` - Директории для каждой функции
  - `spec.md` - Спецификация функции
  - `plan.md` - План реализации
  - `research.md` - Исследования
  - `data-model.md` - Модель данных
  - `quickstart.md` - Быстрый старт
  - `contracts/` - Контракты API
  - `tasks.md` - Задачи реализации
  - `checklists/` - Чек-листы

### `.specify/` - Инструменты спецификаций
- `memory/` - Постоянная память для агентов
- `scripts/` - Bash-скрипты для автоматизации
- `templates/` - Шаблоны для различных документов

### `state/` - Состояние системы
- `planning-phase.schema.json` - Схема для фазы планирования

## Ключевые файлы

### Основные конфигурации
- `QWEN.md` - Основная документация проекта
- `README.md` - Общее описание проекта
- `.gitignore` - Правила игнорирования файлов
- `.mcp.*.json` - Файлы конфигурации MCP

### Скрипты автоматизации
- `scripts/agent-creation/generate-from-template.sh` - Создание агентов из шаблонов
- `scripts/validation/check-yaml-headers.sh` - Проверка YAML заголовков
- `scripts/validation/check-agent-structure.sh` - Проверка структуры агентов
- `scripts/monitoring/update-agents-index.sh` - Обновление индекса агентов
- `scripts/analysis/agent-analyzer.sh` - Анализ агентов
- `scripts/search/agent-search.sh` - Поиск агентов по ключевым словам

### Основные агенты
- `.qwen/agents/work_dev_meta_agent.md` - Мета-агент для создания агентов
- `.qwen/agents/orc_planning_task_analyzer.md` - Оркестратор фазы планирования
- `.qwen/agents/work_planning_task_classifier.md` - Классификатор задач планирования
- `.qwen/agents/work_planning_agent_requirer.md` - Определитель типов агентов
- `.qwen/agents/work_planning_executor_assigner.md` - Назначитель исполнителей

### Основные навыки
- `.qwen/skills/generate-report-header/SKILL.md` - Генерация заголовков отчетов
- `.qwen/skills/run-quality-gate/SKILL.md` - Выполнение контрольных точек качества
- `.qwen/skills/format-markdown-table/SKILL.md` - Форматирование таблиц markdown
- `.qwen/skills/parse-error-logs/SKILL.md` - Разбор логов ошибок
- `.qwen/skills/parse-git-status/SKILL.md` - Разбор статуса Git
- `.qwen/skills/extract-version/SKILL.md` - Извлечение версии

## Архитектурные принципы

### Структура агентов
- Плоская структура (все агенты в одной директории)
- Именование по шаблону: `{типы}_{домен}_{имя}`
- Типы: `orc_` (оркестраторы), `work_` (воркеры)
- Домены: `dev`, `frontend`, `backend`, `testing`, `research`, `security`, `planning`

### Стандарты
- YAML заголовки со строго определенной структурой
- Стандартизированная отчетность
- Шаблон возврата управления
- Использование навыков для повторного использования функций

## Ключевые функции

### 1. Автоматическое создание агентов
- Мета-агент для автоматического создания новых агентов
- Шаблоны для оркестраторов и воркеров
- Скрипты генерации из шаблонов

### 2. Улучшенная навигация по агентам
- Индекс агентов
- Категоризация агентов
- Поиск по ключевым словам
- Анализ функциональности

### 3. Система навыков
- Переиспользуемые функции для агентов
- Стандартизированные форматы ввода/вывода
- Централизованное управление функциональностью

### 4. Контрольные точки качества
- Блокирующие и неблокирующие проверки
- Автоматизированные проверки стандартов
- Интеграция с процессами CI/CD

## Система MCP-конфигураций

Для разных сценариев использования доступны различные конфигурации MCP:
- `.mcp.context7.json` - для поиска контекста и документации библиотек
- `.mcp.supabase.json` - для работы с базами данных Supabase
- `.mcp.sequential.json` - для сложных рассуждений
- `.mcp.playwright.json` - для автоматизации браузерных тестов
- `.mcp.shadcn.json` - для работы с компонентами shadcn
- `.mcp.n8n.json` - для управления рабочими процессами
- `.mcp.default.json` - универсальная конфигурация по умолчанию
EOF

# Обновляем дату в файле
sed -i "s/$(date +%Y)/$(date +%Y)/" "$INDEX_FILE"

log_success "Индекс проекта успешно обновлен: $INDEX_FILE"