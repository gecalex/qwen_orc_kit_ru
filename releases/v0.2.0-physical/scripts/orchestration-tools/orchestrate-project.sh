#!/bin/bash
# Скрипт: scripts/orchestration-tools/orchestrate-project.sh
# Назначение: Главный оркестратор проекта с адаптивным поведением

# Функция для получения состояния проекта
get_project_state() {
    local state_script="./scripts/orchestration-tools/analyze-project-state.sh"

    if [ ! -f "$state_script" ]; then
        echo "ERROR: Скрипт анализа состояния проекта не найден: $state_script"
        exit 1
    fi

    # Запускаем скрипт и получаем код возврата
    # Вывод скрипта подавляем, чтобы не дублировался
    "$state_script" >/dev/null 2>&1
    local exit_code=$?

    case $exit_code in
        10) echo "empty" ;;
        20) echo "existing_code_no_specs" ;;
        30) echo "partial_specification" ;;
        40) echo "full_specification" ;;
        *) echo "unknown" ;;
    esac
}

# Функция для инициализации пустого проекта
initialize_empty_project() {
    echo "=== Инициализация пустого проекта ==="
    echo ""

    echo "Шаг 1: Создание структуры проекта"
    mkdir -p specs
    mkdir -p .specify/memory
    mkdir -p .specify/templates
    mkdir -p .qwen/agents
    mkdir -p state
    mkdir -p docs
    mkdir -p scripts

    echo "Шаг 2: Создание базовой конституции проекта"
    cat > .specify/memory/constitution.md << 'EOF'
# Конституция проекта [НАЗВАНИЕ_ПРОЕКТА]

## Основные принципы

### I. Принципы разработки
- Код должен быть читаемым и понятным
- Все функции должны быть покрыты тестами
- Документация сопровождает код

### II. Стандарты кодирования
- Использование линтеров и форматтеров
- Соглашение об именовании переменных и функций
- Структура проекта по модулям

### III. Тестирование
- Модульные тесты для каждой функции
- Интеграционные тесты для компонентов
- Регрессионное тестирование

### IV. Безопасность
- Проверка ввода данных
- Защита от уязвимостей
- Безопасное хранение данных

## Дополнительные ограничения
- Использование только проверенных библиотек
- Регулярные обновления зависимостей
- Проверка безопасности кода

## Рабочий процесс
- Код-ревью перед мержем
- Автоматические проверки качества
- Документирование изменений

## Управление
- Все изменения фиксируются в логах
- Регулярные ретроспективы
- Непрерывное улучшение процессов

**Версия**: 1.0.0 | **Утверждена**: DATE | **Последнее изменение**: DATE
EOF

    echo "Шаг 3: Предложение создания первой спецификации"
    echo ""
    echo "Теперь рекомендуется создать первую спецификацию функции."
    echo "Вы можете использовать команду: speckit.specify \"описание вашей первой функции\""
    echo ""
    echo "Пример: speckit.specify \"Создать систему аутентификации пользователей\""
}

# Функция для анализа существующего проекта без спецификаций
analyze_existing_project() {
    echo "=== Анализ существующего проекта без спецификаций ==="
    echo ""

    echo "Шаг 1: Сбор информации о проекте"
    echo "Обнаруженные языки программирования и файлы:"
    find . -name "*.py" -o -name "*.js" -o -name "*.ts" -o -name "*.java" -o -name "*.cpp" -o -name "*.c" -o -name "*.cs" -o -name "*.rb" -o -name "*.go" -o -name "*.rs" -o -name "*.php" | head -20

    echo ""
    echo "Шаг 2: Анализ структуры проекта"
    echo "Структура директорий:"
    ls -la

    echo ""
    echo "Шаг 3: Предложение создания реверс-инжиниринга спецификаций"
    echo ""
    echo "Рекомендуется создать спецификации на основе существующего кода."
    echo "Это можно сделать с помощью анализа кода и выявления основных компонентов."
    echo ""
    echo "Вы можете использовать агентов для анализа кода:"
    echo "1. bug-hunter для поиска потенциальных проблем"
    echo "2. code-quality-checker для анализа структуры"
    echo "3. tech-translator-ru для документирования"
}

# Функция для работы с проектом с частичными спецификациями
handle_partial_specifications() {
    echo "=== Работа с проектом с частичными спецификациями ==="
    echo ""

    echo "Шаг 1: Анализ существующих спецификаций"
    if [ -d "specs" ]; then
        echo "Найдены следующие спецификации:"
        find specs -name "spec.md" | head -10
    else
        echo "Директория specs не найдена"
    fi

    echo ""
    echo "Шаг 2: Проверка соответствия реализации спецификациям"
    echo "Необходимо проверить, соответствует ли код существующим спецификациям"

    echo ""
    echo "Шаг 3: Предложение доработки спецификаций"
    echo "Рекомендуется:"
    echo "- Заполнить пробелы в существующих спецификациях"
    echo "- Проверить полноту требований"
    echo "- Обновить архитектурные решения"
}

# Функция для работы с проектом с полными спецификациями
handle_full_specifications() {
    echo "=== Работа с проектом с полными спецификациями ==="
    echo ""

    echo "Шаг 1: Проверка соответствия реализации спецификациям"
    echo "Запуск проверки соответствия реализации спецификациям..."

    echo ""
    echo "Шаг 2: Проверка контрольных точек качества"
    echo "Проверка Gate 5: Pre-Implementation Checks"
    ./scripts/run_quality_gate.sh 5 specs/ 2>/dev/null || echo "Не найдены спецификации для проверки"

    echo ""
    echo "Шаг 3: Продолжение стандартного процесса Speckit"
    echo "Проект готов к стандартному процессу разработки на основе спецификаций"
}

# Основная логика оркестратора
echo "=== Центральный оркестратор проекта ==="
echo ""

# Определяем состояние проекта
PROJECT_STATE=$(get_project_state)
echo "Обнаруженное состояние проекта: $PROJECT_STATE"
echo ""

# Адаптируем поведение в зависимости от состояния
case $PROJECT_STATE in
    "empty")
        initialize_empty_project
        ;;
    "existing_code_no_specs")
        analyze_existing_project
        ;;
    "partial_specification")
        handle_partial_specifications
        ;;
    "full_specification")
        handle_full_specifications
        ;;
    *)
        echo "Неопределенное состояние проекта: $PROJECT_STATE"
        echo "Требуется ручной анализ и определение подходящего процесса"
        ;;
esac

echo ""
echo "=== Завершение анализа оркестратором ==="
