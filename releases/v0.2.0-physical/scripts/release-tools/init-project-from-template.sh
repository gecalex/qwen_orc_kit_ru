#!/bin/bash
# Скрипт: scripts/release-tools/init-project-from-template.sh
# Назначение: Инициализация нового проекта из шаблона

set -e  # Прерывать выполнение при ошибках

PROJECT_NAME="$1"
ORGANIZATION="$2"

if [ -z "$PROJECT_NAME" ]; then
    echo "Использование: $0 <имя-проекта> [организация]"
    echo "Пример: $0 my-new-project my-org"
    exit 1
fi

if [ -z "$ORGANIZATION" ]; then
    ORGANIZATION="organization"
fi

echo "=== Инициализация нового проекта: $PROJECT_NAME ==="

# Проверка, существует ли директория проекта
if [ -d "$PROJECT_NAME" ]; then
    echo "Ошибка: Директория $PROJECT_NAME уже существует"
    exit 1
fi

# Создание директории проекта
mkdir -p "$PROJECT_NAME"
cd "$PROJECT_NAME"

# Копирование содержимого шаблона (предполагается, что мы запускаем из шаблона)
# В реальной ситуации этот скрипт будет частью шаблона
echo "Копирование файлов шаблона..."
cp -r /path/to/template/. . 2>/dev/null || {
    # Если запускается из шаблона, копируем текущую директорию
    echo "Копирование файлов из шаблона..."
    # В реальности здесь будет копирование из шаблона
    # Для демонстрации просто продолжаем с текущей структуры
}

# Замена плейсхолдеров на реальные значения
echo "Замена плейсхолдеров на значения проекта..."

# Заменяем плейсхолдеры в файлах
find . -type f \( -name "*.md" -o -name "*.txt" -o -name "*.toml" -o -name "*.py" -o -name "*.sh" -o -name "*.json" -o -name "*.yaml" -o -name "*.yml" \) -exec sed -i.bak "s/{{PROJECT_NAME}}/$PROJECT_NAME/g" {} \; 2>/dev/null || true
find . -type f \( -name "*.md" -o -name "*.txt" -o -name "*.toml" -o -name "*.py" -o -name "*.sh" -o -name "*.json" -o -name "*.yaml" -o -name "*.yml" \) -exec sed -i.bak "s/{{ORGANIZATION_NAME}}/$ORGANIZATION/g" {} \; 2>/dev/null || true
find . -type f \( -name "*.md" -o -name "*.txt" -o -name "*.toml" -o -name "*.py" -o -name "*.sh" -o -name "*.json" -o -name "*.yaml" -o -name "*.yml" \) -exec sed -i.bak "s/{{PROJECT_TEMPLATE_NAME}}/$PROJECT_NAME/g" {} \; 2>/dev/null || true

# Удаление временных файлов редактирования
find . -name "*.bak" -delete 2>/dev/null || true

# Инициализация Git
echo "Инициализация Git репозитория..."
git init
git add .
git commit -m "feat: initial commit from Qwen Code Orchestrator Kit template"

# Создание начальной структуры проекта
echo "Создание начальной структуры проекта..."
mkdir -p specs/001-initial-feature
mkdir -p state
mkdir -p docs/project-specific

# Создание начальной спецификации
cat > specs/001-initial-feature/spec.md << SPEC_EOF
# Спецификация: Основа проекта $PROJECT_NAME

## Краткое описание
Создание основы проекта $PROJECT_NAME с базовой функциональностью и архитектурой.

## Контекст
Необходимо создать базовую структуру проекта $PROJECT_NAME с минимальной функциональностью для демонстрации работы шаблона Qwen Code Orchestrator Kit.

## Акторы
- Разработчик: создает и развивает проект
- Пользователь: использует функциональность проекта (если применимо)

## Требования

### Функциональные требования
1. Проект должен иметь базовую структуру, соответствующую шаблону
2. Проект должен включать минимальный набор агентов
3. Проект должен содержать пример спецификации и задач

### Нефункциональные требования
1. Код должен соответствовать стандартам, определенным в конституции проекта
2. Проект должен быть готов к использованию процесса Speckit
3. Все компоненты должны быть документированы

## Сценарии использования
1. Разработчик клонирует шаблон
2. Запускает скрипт инициализации
3. Получает готовый проект с базовой структурой
4. Может сразу начать добавлять новую функциональность через процесс Speckit

## Условия успеха
- Проект успешно инициализирован
- Все базовые компоненты работают
- Можно создать первую спецификацию через speckit.specify
- Можно запустить базовую проверку качества

## Ограничения
- Использовать только компоненты из шаблона
- Следовать архитектурным принципам шаблона
- Поддерживать совместимость с системой агентов

## Предположения
- Разработчик имеет доступ к Qwen Code CLI
- Разработчик знаком с процессом Speckit
- Система разработки настроена должным образом

## Возможные риски
- Несовместимость с новыми версиями инструментов
- Отсутствие обновлений шаблона
SPEC_EOF

# Создание начального плана
cat > specs/001-initial-feature/plan.md << PLAN_EOF
# План реализации: Основа проекта $PROJECT_NAME

**Ветка**: \`001-initial-project-setup\` | **Дата**: $(date +%Y-%m-%d) | **Спецификация**: [specs/001-initial-feature/spec.md](../001-initial-feature/spec.md)

**Входные данные**: Спецификация функции из \`specs/001-initial-feature/spec.md\`

## Резюме

Реализация базовой структуры проекта $PROJECT_NAME с минимальной функциональностью для демонстрации шаблона Qwen Code Orchestrator Kit.

## Технический контекст

**Язык/Версия**: Зависит от типа проекта
**Основные зависимости**: Зависит от типа проекта
**Хранилище**: Зависит от требований
**Тестирование**: Зависит от архитектуры
**Целевая платформа**: Зависит от проекта
**Тип проекта**: Определяется при разработке
**Цели производительности**: Определяются требованиями
**Ограничения**: Определяются архитектурой
**Масштаб/Объем**: Определяется требованиями

## Проверка конституции

Проект должен соответствовать принципам, определенным в конституции проекта.

## Структура проекта

### Документация (эта функция)

\`\`\`text
specs/001-initial-feature/
├── plan.md              # Этот файл
├── research.md          # Исследования и решения (создается при необходимости)
├── data-model.md        # Модель данных (создается при необходимости)
├── quickstart.md        # Быстрый старт (создается при необходимости)
├── contracts/           # Контракты API (создается при необходимости)
└── tasks.md             # Задачи реализации (создается командой speckit.tasks)
\`\`\`

### Исходный код (корень репозитория)
\`\`\`
src/
├── __init__.py
└── main.py              # Точка входа (для Python проектов)

tests/
├── __init__.py
├── test_main.py         # Базовые тесты
└── conftest.py          # Конфигурация тестов (если применимо)

docs/
├── architecture/        # Архитектурная документация
└── guides/              # Руководства пользователя
\`\`\`

**Решение по структуре**: Структура определяется типом проекта, базовая структура создана в шаблоне

## Отслеживание сложности

> **Заполняйте ТОЛЬКО если проверка конституции имеет нарушения, которые необходимо обосновать**

| Нарушение | Почему необходимо | Более простая альтернатива отклонена потому что |
|-----------|------------------|---------------------------------------------|
| (не применимо для базовой структуры) | (не применимо) | (не применимо) |
PLAN_EOF

echo "=== Инициализация проекта завершена ==="
echo "Проект $PROJECT_NAME создан и готов к использованию."
echo ""
echo "Следующие шаги:"
echo "1. Ознакомьтесь с созданной спецификацией в specs/001-initial-feature/spec.md"
echo "2. При необходимости уточните спецификацию: speckit.clarify"
echo "3. Создайте план реализации: speckit.plan"
echo "4. Сгенерируйте задачи: speckit.tasks"
echo "5. Начните реализацию: speckit.implement"
echo ""
echo "Для получения дополнительной информации см. docs/architecture/specification-driven-development.md"
