#!/bin/bash
# Скрипт проверки безопасности
# Назначение: Проверяет код на наличие уязвимостей безопасности
# Блокирующая: true (останавливает выполнение при критических уязвимостях)

set -e

echo "=== Проверка безопасности ==="

# Проверка наличия скрипта безопасности
if [ ! -f "scripts/security-tools/security-check.sh" ]; then
    echo "⚠️  Скрипт безопасности не найден, пропускаем проверку"
    exit 0
fi

# Запуск проверки безопасности
echo "Запуск скрипта безопасности..."
scripts/security-tools/security-check.sh

SECURITY_RESULT=$?

if [ $SECURITY_RESULT -eq 0 ]; then
    echo "✅ Проверка безопасности пройдена"
    exit 0
else
    echo "❌ Обнаружены критические уязвимости безопасности"
    echo "🚨 БЛОКИРУЮЩАЯ проверка не пройдена - выполнение остановлено"
    exit 1
fi